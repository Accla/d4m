function s = numel(A)
%numel: Returns the number of row times the number of columns in an associative array.
%Associative array user function.
%  Usage:
%    s = numel(A)
%  Inputs:
%    A = associative array
%  Outputs:
%    s =  number of row times the number of columns in an associative array

   s = numel(A.A);

end

